    public void llenar(Library n){
SongNode aux = n.First;
cSongs.removeAllItems();
while(aux!=null){
            cSongs.addItem(aux.Name);
            aux=aux.next;
        }
   
}
    public void llenar2(Library n){
SongNode aux = n.First;
sSongs.removeAllItems();
while(aux!=null){
            sSongs.addItem(aux.Artist +"  "+ aux.Name);
            aux=aux.next;
        }
      
}
