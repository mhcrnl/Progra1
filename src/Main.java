    /**
     * @param args the command line arguments
     */
    public void llenar(Library n){
SongNode aux = n.First;
cSongs.removeAllItems();
while(aux!=n.Last){
            cSongs.addItem(aux.Artist +"  "+ aux.Name);
            aux=aux.next;
        }
        if (aux==n.Last)
            cSongs.addItem(aux.Artist +"  "+ aux.Name);
}
